package com.alfan.kotlin.modul1

fun main() {
    //penggunaan var dan val
    //var dapat diganti nilainya sedangkan val sudah static

    var company: String = "Yeay"
    company = "Ranger go"
    println(company)

    val gg: String = "Fans"
    print(gg)

    println (company + gg)

    val valuA : Int =40
    // bisa melakukan inisialisasi variabel tanpa menyebut tipenya
    val valuB =20
    println(valuA + valuB)

}